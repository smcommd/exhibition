type StudentRecord = (typeof realStudentData)[number];

const koreanNameCollator = new Intl.Collator('ko-KR', { sensitivity: 'base' });
const HANGUL_INITIALS = ['ㄱ','ㄲ','ㄴ','ㄷ','ㄸ','ㄹ','ㅁ','ㅂ','ㅃ','ㅅ','ㅆ','ㅇ','ㅈ','ㅉ','ㅊ','ㅋ','ㅌ','ㅍ','ㅎ'] as const;
export const hangulInitialOrder = [...HANGUL_INITIALS];
const HANGUL_BASE = 0xac00;
const HANGUL_LAST = 0xd7a3;
const HANGUL_JUNGSEONG_JONGSEONG = 21 * 28;

const getHangulInitial = (name: string): string => {
  const firstChar = name?.[0];
  if (!firstChar) return '#';
  const code = firstChar.charCodeAt(0);
  if (code < HANGUL_BASE || code > HANGUL_LAST) {
    return firstChar;
  }
  const initialIndex = Math.floor((code - HANGUL_BASE) / HANGUL_JUNGSEONG_JONGSEONG);
  return HANGUL_INITIALS[initialIndex] ?? firstChar;
};

// 이름 기준 정렬 + 동명이인 그룹을 별도로 제공
export const realStudentDataSortedByName = [...realStudentData].sort((a, b) =>
  koreanNameCollator.compare(a.name, b.name)
);

export const studentDataByName = realStudentDataSortedByName.reduce<Record<string, StudentRecord[]>>(
  (acc, student) => {
    if (!acc[student.name]) {
      acc[student.name] = [];
    }
    acc[student.name].push(student);
    return acc;
  },
  {},
);

export const studentDataByInitial = realStudentDataSortedByName.reduce<Record<string, StudentRecord[]>>(
  (acc, student) => {
    const initial = getHangulInitial(student.name);
    if (!acc[initial]) {
      acc[initial] = [];
    }
    acc[initial].push(student);
    return acc;
  },
  {},
);

export const realStudentDataGroupedByInitial = HANGUL_INITIALS.map((initial) => ({
  initial,
  students: studentDataByInitial[initial] ?? [],
})).filter(({ students }) => students.length > 0);

